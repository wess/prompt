/// A slider whose zero displays as "auto".
fn auto_slider(
    key: &'static str,
    label: &'static str,
    desc: &'static str,
    section: Section,
    get: fn(&config::Options) -> f32,
    range: (f32, f32, f32),
) -> Setting {
    let (min, max, step) = range;
    Setting {
        key,
        label,
        desc,
        section,
        control: Control::Slider(Slider { get, min, max, step, int: true, auto_zero: true }),
    }
}
